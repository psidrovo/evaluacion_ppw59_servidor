package ec.edu.ups.ppw.ppw59.service;

public class Respuesta {

	private int codig;
	private String mensaje;
	
	public int getCodig() {
		return codig;
	}
	public void setCodig(int codig) {
		this.codig = codig;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
}
